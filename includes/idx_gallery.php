<script>
(function () {

    // ── Lightbox ──
    const lightbox   = document.getElementById('idxLightbox');
    const lbImg      = document.getElementById('lightboxImg');
    const lbClose    = document.getElementById('lightboxClose');
    const lbPrev     = document.getElementById('lightboxPrev');
    const lbNext     = document.getElementById('lightboxNext');
    let currentIdx   = 0;
    const allCards   = [...document.querySelectorAll('.idx-frame-card')];

    function openLightbox(idx) {
        currentIdx = idx;
        lbImg.src  = allCards[currentIdx].querySelector('img').src;
        lightbox.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    function closeLightbox() {
        lightbox.classList.remove('active');
        document.body.style.overflow = '';
    }

    lbClose.addEventListener('click', closeLightbox);
lightbox.addEventListener('click', e => { if (e.target === lightbox) closeLightbox(); });

lbPrev.addEventListener('click', () => {
    currentIdx = (currentIdx - 1 + allCards.length) % allCards.length;
    lbImg.src  = allCards[currentIdx].querySelector('img').src;
});

lbNext.addEventListener('click', () => {
    currentIdx = (currentIdx + 1) % allCards.length;
    lbImg.src  = allCards[currentIdx].querySelector('img').src;
});

allCards.forEach((card, i) => {
    card.querySelector('.idx-frame-img-wrap').addEventListener('click', () => openLightbox(i));
});

document.addEventListener('keydown', e => {
    if (!lightbox.classList.contains('active')) return;
    if (e.key === 'Escape')     closeLightbox();
    if (e.key === 'ArrowLeft')  lbPrev.click();
    if (e.key === 'ArrowRight') lbNext.click();
});

    // ── Scroll-reveal ──
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('idx-frame-visible');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.1 });

    cards.forEach(card => observer.observe(card));
})();
</script>